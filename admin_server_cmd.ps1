# obtenir taille dossiers
Get-ChildItem -Path c:\ -Depth 2  | Measure-Object -Property Length -Sum | Select-Object @{Name="Size";Expression={"{0:N2} {1}" -f ($_.Sum / 1GB), "GB"}}
