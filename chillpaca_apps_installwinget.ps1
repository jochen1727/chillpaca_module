#script installer applications winget
#Variable desactiver erreurs et avertissements
$ErrorActionPreference = "SilentlyContinue"
$WarningPreference ="SilentlyContinue"

function install-apps-winget
{

[CmdletBinding()]
 param(
         [Parameter(Mandatory,ParameterSetName = 'type')]
         [ValidateSet('base')]
         [string]$type
     )
begin
{
Get-Job | where state -ne running | Remove-Job
winget source update
$baseobject=@(
     [pscustomobject]@{application='Notepad++.Notepad++';arguments=''}
     [pscustomobject]@{application='VideoLAN.VLC'; arguments=''}
     [pscustomobject]@{application='Google.Chrome'; arguments=''}
     [pscustomobject]@{application='Microsoft.PowerShell'; arguments=''}
     [pscustomobject]@{application='7zip.7zip'; arguments=''}
     [pscustomobject]@{application='Microsoft.Teams.Classic'; arguments=''}
)
$base=@()
$base=$baseobject |ConvertTo-Json | ConvertFrom-Json -AsHashtable

}
process
{
foreach ($app in $base)
{  
Start-job -ArgumentList $app -name $app.application -ScriptBlock {winget install --id $using:app.application  --scope machine }
}



}
end
{

}
}
install-apps-winget -type base
